// Replace string
function replace(text, array) {
    var returnValue = text;
    var alphabet = "abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ";
    for (var i = 0; i < alphabet.length; i++) {
        returnValue = returnValue.replaceAll(alphabet[i], array[i]);
    };
    return returnValue;
}

// Copy to clipboard
function copy(text) {
    document.getElementsByClassName("copied")[0].classList.remove("popup")
    navigator.clipboard.writeText(text).then(() => {
        document.getElementsByClassName("copied")[0].classList.add("popup")
    })
}

// When button click
document.getElementById("start_button").addEventListener("click", function () {
    var text = document.getElementsByClassName("input")[0].value;
    var list = document.getElementsByClassName("output_list")[0];
    list.innerHTML = ""
    document.getElementsByClassName("copied")[0].classList.remove("popup")
    for (var i = 0; i < dict.length; i++) {
        var result = replace(text, dict[i])
        list.innerHTML += `<div class="output"><p class="cursive_output">${result}</p><button class="clipboard" data-copy="${result}"><i class="far fa-clipboard"></i></button></div>`
    }
    document.getElementsByClassName("effect")[0].classList.add("open");
    document.querySelectorAll(".clipboard").forEach(btn => {
        btn.addEventListener("click", function (evt) {
            copy(evt.target.dataset.copy);
        })
    })
})

// Close Modal
document.getElementsByClassName("close")[0].addEventListener("click", function () {
    document.getElementsByClassName("effect")[0].classList.remove("open");
    document.getElementsByClassName("input")[0].value = "";
})